export const mapNslToStandard = (text: string) => {
    if (!text) return "";

    // Шаг 1: Сжатие окончаний полных прилагательных (-ый/-ий -> -y/-i)
    const processedText = text
        .replace(/ый(?![▲\p{L}])/gu, 'y')
        .replace(/ий(?![▲\p{L}])/gu, 'i')
        .replace(/ЫЙ(?![▲\p{L}])/gu, 'Y')
        .replace(/ИЙ(?![▲\p{L}])/gu, 'I');

    // Базовый словарь фиксированных замен (без мягких гласных)
    const baseRules = {
        'Б': 'B', 'б': 'b', 'В': 'V', 'в': 'v', 'Г': 'G', 'г': 'g',
        'Ґ': 'G', 'ґ': 'g', 'Д': 'D', 'д': 'd', 'Џ': 'Dž', 'џ': 'dž',
        'Ж': 'Ž', 'ж': 'ž', 'З': 'Z', 'з': 'z', 'Ѕ': 'Dz', 'ѕ': 'dz',
        'П': 'P', 'п': 'p', 'Ф': 'F', 'ф': 'f', 'Х': 'H', 'х': 'h',
        'К': 'K', 'к': 'k', 'Т': 'T', 'т': 't', 'Ч': 'Č', 'ч': 'č',
        'Ш': 'Š', 'ш': 'š', 'С': 'S', 'с': 's', 'Ц': 'C', 'ц': 'c',
        'Ј': 'I', 'ј': 'i', 'Л': 'L', 'л': 'l', 'М': 'M', 'м': 'm',
        'Н': 'N', 'н': 'n', 'Р': 'R', 'р': 'r', 'Ҏ': 'R', '҂': '',
        'Й': 'J', 'й': 'j',
        'Á': 'A', 'á': 'a', 'Å': 'А', 'å': 'a', 'Э': 'E', 'э': 'e', 'Ы': 'Y', 'ы': 'y',
        'Ӯ': 'V', 'ӯ': 'v', 'Ъ': '', 'ъ': '',  'Ó': 'O', 'ó': 'o',
        'А': 'A', 'а': 'a', 'Е': 'E', 'е': 'e', 'И': 'I', 'и': 'i',
        'У': 'U', 'у': 'u', 'Ô': 'O', 'ô': 'o', 'О': 'O', 'о': 'o',
        'І': 'J', 'і': 'j', 'Ї': 'Ji', 'ї': 'ji', 'Ь': 'J', 'ь': 'j',
        'Ѣ': 'E', 'ѣ': 'e', 'Ѧ': 'E', 'ѧ': 'e', 'Ѫ': 'U', 'ѫ': 'u',
        'Į': 'I', 'į': 'i', 'Щ': 'Št', 'щ': 'št', 'Ќ': 'Ks', 'ќ': 'ks',
        'Ѱ': 'Ps', 'ѱ': 'ps', 'Ѳ': 'T', 'ѳ': 't'
    };

    // Набор мягких гласных и их раскрытие (пары: [без i, с i])
    const softVowels = {
        'Я': ['Ja', 'A'], 'я': ['ja', 'a'],
        'Ю': ['Ju', 'U'], 'ю': ['ju', 'u'],
        'Є': ['Je', 'E'], 'є': ['je', 'e'],
        'Ё': ['Jo', 'O'], 'ё': ['jo', 'o']
    };

    let result = "";

    // Шаг 2: Посимвольный разбор с проверкой контекста
    for (let i = 0; i < processedText.length; i++) {
        const char = processedText[i];

        if (softVowels.hasOwnProperty(char)) {
            // Проверяем, какой символ был обработан прямо перед этим в результирующей строке
            const lastOutputChar = result.length > 0 ? result[result.length - 1].toLowerCase() : '';

            // Если предыдущий выведенный символ - это латинская 'i', берем твёрдый вариант без 'i'
            if (lastOutputChar === 'i') {
                result += softVowels[char][1];
            } else {
                // Иначе раскрываем как i + твёрдая гласная
                result += softVowels[char][0];
            }
        } else {
            // Для всех остальных букв применяем стандартный словарь
            result += baseRules.hasOwnProperty(char) ? baseRules[char] : char;
        }
    }

    return result.toLowerCase();
}

export const mapNslToEtymologized = (text: string) => {
    if (!text) return "";

    // Шаг 1: Контекстное сжатие окончаний полных прилагательных согласно этимологии (-ый/-ий -> -yj/-ij)
    const processedText = text
        .replace(/ый(?![▲\p{L}])/gu, 'y')
        .replace(/ий(?![▲\p{L}])/gu, 'i')
        .replace(/ЫЙ(?![▲\p{L}])/gu, 'Y')
        .replace(/ИЙ(?![▲\p{L}])/gu, 'I');

    // Базовый этимологический словарь фиксированных замен (без мягких гласных)
    const baseRules = {
        'Б': 'B', 'б': 'b', 'В': 'V', 'в': 'v', 'Г': 'G', 'г': 'g',
        'Ґ': 'G', 'ґ': 'g', 'Д': 'D', 'д': 'd', 'Џ': 'Dž', 'џ': 'dž',
        'Ж': 'Ž', 'ж': 'ž', 'З': 'Z', 'з': 'z', 'Ѕ': 'Dz', 'ѕ': 'dz',
        'П': 'P', 'п': 'p', 'Ф': 'F', 'ф': 'f', 'Х': 'H', 'х': 'h',
        'К': 'K', 'к': 'k', 'Т': 'T', 'т': 't', 'Ч': 'Č', 'ч': 'č',
        'Ш': 'Š', 'ш': 'š', 'С': 'S', 'с': 's', 'Ц': 'C', 'ц': 'c',
        'Ј': 'J', 'ј': 'j', 'Л': 'L', 'л': 'l', 'М': 'M', 'м': 'm',
        'Н': 'N', 'н': 'n', 'Р': 'R', 'р': 'r', 'Ҏ': 'R', '҂': '',
        'Й': 'J', 'й': 'j',
        'Ў': 'V', 'ў': 'v',
        'Á': 'A', 'á': 'a', 'Э': 'E', 'э': 'e', 'Ы': 'Y', 'ы': 'y',
        'Ӯ': 'V', 'ӯ': 'v', 'Ъ': '',  'ъ': '',  'Ó': 'O', 'ó': 'o',
        'А': 'A', 'а': 'a', 'Е': 'E', 'е': 'e', 'И': 'I', 'и': 'i',
        'У': 'U', 'у': 'u', 'Ô': 'O', 'ô': 'o', 'О': 'O', 'о': 'o',
        'І': 'I', 'і': 'i', 'Ї': 'Ji', 'ї': 'ji', 'Ь': 'J',  'ь': 'j',

        // --- Сохранение исторических и носовых графем ---
        'Ѣ': 'Ě', 'ѣ': 'ě', // Ять
        'Ѧ': 'Ę', 'ѧ': 'ę', // Малый юс (E с огонеком)
        'Į': 'Ę', 'į': 'ę', // Носовое I также кодируется через Ę
        'Ѫ': 'Ǫ', 'ѫ': 'ǫ', // Большой юс (O с огонеком)

        'Щ': 'Št', 'щ': 'št', 'Ќ': 'Ks', 'ќ': 'ks',
        'Ѱ': 'Ps', 'ѱ': 'ps', 'Ѳ': 'T', 'ѳ': 't'
    };

    // Мягкие гласные раскрываются через 'j' (этимологический стандарт)
    // Пары: [без i перед буквой -> 'j'+гласная, с i перед буквой -> просто гласная]
    const softVowels = {
        'Я': ['Ja', 'A'], 'я': ['ja', 'a'],
        'Ю': ['Ju', 'U'], 'ю': ['ju', 'u'],
        'Є': ['Je', 'E'], 'є': ['je', 'e'],
        'Ё': ['Jo', 'O'], 'ё': ['jo', 'o']
    };

    let result = "";

    // Шаг 2: Посимвольный разбор с проверкой контекста
    for (let i = 0; i < processedText.length; i++) {
        const char = processedText[i];

        if (softVowels.hasOwnProperty(char)) {
            const lastOutputChar = result.length > 0 ? result[result.length - 1].toLowerCase() : '';

            // Если перед мягкой гласной идет 'i' или 'j', то раскрываем БЕЗ дополнительного 'j'
            if (lastOutputChar === 'i' || lastOutputChar === 'j') {
                result += softVowels[char][1];
            } else {
                // В обычном контексте добавляем этимологический 'j'
                result += softVowels[char][0];
            }
        } else {
            result += baseRules.hasOwnProperty(char) ? baseRules[char] : char;
        }
    }

    return result;
}
